import numpy as np


def get_pose_range():
    pose_range = np.float32([  # unit: degree
        [45, 45, 45],  # 1
        [45, 45, 45],  # 2
        [45, 45, 45],  # 3
        [180, 45, 90],  # 4
        [180, 45, 90],  # 5
        [45, 45, 45],  # 6
        [180, 45, 45],  # 7
        [180, 45, 45],  # 8
        [45, 45, 45],  # 9
        [90, 90, 45],  # 10
        [90, 90, 45],  # 11
        [45, 45, 45],  # 12
        [45, 90, 45],  # 13
        [45, 90, 45],  # 14
        [90, 45, 90],  # 15
        [45, 180, 45],  # 16
        [45, 180, 45],  # 17
        [45, 180, 180],  # 18
        [45, 180, 180],  # 19
        [45, 180, 180],  # 20
        [45, 180, 180],  # 21
    ])
    assert pose_range.shape == (21, 3)
    pose_range = pose_range / 180 * np.pi
    return pose_range
